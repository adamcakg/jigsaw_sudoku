    public int[] getQuadrant(int col, int row) {
        if(getTotalQuadrants() == null)
            return null;
        int colOfQuadrant, rowOfquadrant;
        for(int i = 0 ; i < 9 ; i++){
            for(int j = 0 ; j < 9 ; j ++){
                int quadrantField = getTotalQuadrants()[i][j];
                colOfQuadrant = quadrantField % 10;
                quadrantField = quadrantField / 10;
                rowOfquadrant = quadrantField % 10;
                if(colOfQuadrant == col && rowOfquadrant == row)
                    return totalQuadrants[i];
            }
        }
        return null;
    }

